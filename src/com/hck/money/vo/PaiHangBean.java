public class PaiHangBean {
private long uid;
private String userName;
private String tx;
private String tgSize;
private String tgMoney;
private String allMoney;
public long getUid() {
	return uid;
}
public void setUid(long uid) {
	this.uid = uid;
}
public String getUserName() {
	return userName;
}
public void setUserName(String userName) {
	this.userName = userName;
}
public String getTx() {
	return tx;
}
public void setTx(String tx) {
	this.tx = tx;
}
public String getTgSize() {
	return tgSize;
}
public void setTgSize(String tgSize) {
	this.tgSize = tgSize;
}
public String getTgMoney() {
	return tgMoney;
}
public void setTgMoney(String tgMoney) {
	this.tgMoney = tgMoney;
}
public String getAllMoney() {
	return allMoney;
}
public void setAllMoney(String allMoney) {
	this.allMoney = allMoney;
}

}
